import fs from 'node:fs';
import path from 'node:path';

const cwd = process.cwd();
const openapiPath = path.join(cwd, 'openapi', 'cosmosh.openapi.yaml');
const protocolOutputPath = path.join(cwd, 'src', 'protocol.ts');

const source = fs.readFileSync(openapiPath, 'utf8');
const lines = source.split(/\r?\n/);

const pathEntries = [];
let currentPath = null;

for (const line of lines) {
  const pathMatch = line.match(/^  (\/[^:]+):\s*$/);
  if (pathMatch) {
    currentPath = pathMatch[1];
    continue;
  }

  const operationIdMatch = line.match(/^      operationId:\s*([A-Za-z0-9_\-]+)\s*$/);
  if (operationIdMatch && currentPath) {
    pathEntries.push([operationIdMatch[1], currentPath]);
  }
}

const readFirstMatch = (pattern, fallback) => {
  const matched = source.match(pattern);
  return matched?.[1] ?? fallback;
};

const toConstKey = (value) => {
  const normalized = value
    .toLowerCase()
    .replace(/[^a-z0-9]+([a-z0-9])/g, (_match, char) => char.toUpperCase())
    .replace(/[^a-zA-Z0-9]/g, '');

  return normalized || 'unknownCode';
};

const dedupeEntries = (entries) => {
  const map = new Map();

  for (const [key, value] of entries) {
    if (!map.has(key)) {
      map.set(key, value);
    }
  }

  return [...map.entries()];
};

const extractCapabilities = () => {
  const capabilitiesMatch = source.match(/capabilities:[\s\S]*?enum:\s*\[(.*?)\]/m);
  if (!capabilitiesMatch?.[1]) {
    return ['ssh', 'sftp'];
  }

  return capabilitiesMatch[1]
    .split(',')
    .map((item) => item.trim().replace(/^'+|'+$/g, '').replace(/^"+|"+$/g, ''))
    .filter(Boolean);
};

const extractApiCodes = () => {
  const pattern = /enum:\s*\[([\s\S]*?)\]/g;
  const detectedCodes = [];

  for (const match of source.matchAll(pattern)) {
    const raw = match[1] ?? '';
    const values = raw
      .split(',')
      .map((item) => item.trim().replace(/^'+|'+$/g, '').replace(/^"+|"+$/g, ''))
      .filter((item) => /^[A-Z0-9_]+$/.test(item));

    for (const value of values) {
      detectedCodes.push([toConstKey(value), value]);
    }
  }

  detectedCodes.push(['authInvalidToken', 'AUTH_INVALID_TOKEN']);
  detectedCodes.push(['testPingOk', 'TEST_PING_OK']);

  return dedupeEntries(detectedCodes);
};

const internalTokenHeaderName = readFirstMatch(/name:\s*(x-cosmosh-internal-token)/, 'x-cosmosh-internal-token');
const localeHeaderName = readFirstMatch(/LocaleHeader:[\s\S]*?name:\s*(x-cosmosh-locale)/m, 'x-cosmosh-locale');
const capabilities = extractCapabilities();
const apiCodes = extractApiCodes();

const formatPathEntries = pathEntries.map(([operationId, routePath]) => `  ${operationId}: '${routePath}',`).join('\n');
const formatCodeEntries = apiCodes.map(([key, value]) => `  ${key}: '${value}',`).join('\n');
const formatCapabilities = capabilities.map((item) => `'${item}'`).join(', ');

const output = `/**\n * This file is auto-generated from openapi/cosmosh.openapi.yaml.\n * Do not edit this file directly. Run "pnpm --filter @cosmosh/api-contract generate" instead.\n */\n\nexport const API_PATHS = {\n${formatPathEntries}\n} as const;\n\nexport const API_HEADERS = {\n  internalToken: '${internalTokenHeaderName}',\n  locale: '${localeHeaderName}',\n} as const;\n\nexport const API_CODES = {\n${formatCodeEntries}\n} as const;\n\nexport const API_CAPABILITIES = [${formatCapabilities}] as const;\n`;

fs.writeFileSync(protocolOutputPath, output, 'utf8');
console.log(`Generated protocol constants from OpenAPI -> ${path.relative(cwd, protocolOutputPath)}`);
