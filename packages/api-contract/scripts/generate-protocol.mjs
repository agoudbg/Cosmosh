import fs from 'node:fs';
import path from 'node:path';

const cwd = process.cwd();
const openapiPath = path.join(cwd, 'openapi', 'cosmosh.openapi.yaml');
const protocolOutputPath = path.join(cwd, 'src', 'protocol.ts');

const source = fs.readFileSync(openapiPath, 'utf8');
const lines = source.split(/\r?\n/);

const pathEntries = [];
let currentPath = null;

for (const line of lines) {
  const pathMatch = line.match(/^  (\/[^:]+):\s*$/);
  if (pathMatch) {
    currentPath = pathMatch[1];
    continue;
  }

  const operationIdMatch = line.match(/^      operationId:\s*([A-Za-z0-9_\-]+)\s*$/);
  if (operationIdMatch && currentPath) {
    pathEntries.push([operationIdMatch[1], currentPath]);
    currentPath = null;
  }
}

const readFirstMatch = (pattern, fallback) => {
  const matched = source.match(pattern);
  return matched?.[1] ?? fallback;
};

const extractCapabilities = () => {
  const capabilitiesMatch = source.match(/capabilities:[\s\S]*?enum:\s*\[(.*?)\]/m);
  if (!capabilitiesMatch?.[1]) {
    return ['ssh', 'sftp'];
  }

  return capabilitiesMatch[1]
    .split(',')
    .map((item) => item.trim().replace(/^'+|'+$/g, '').replace(/^"+|"+$/g, ''))
    .filter(Boolean);
};

const internalTokenHeaderName = readFirstMatch(/name:\s*(x-cosmosh-internal-token)/, 'x-cosmosh-internal-token');
const localeHeaderName = readFirstMatch(/LocaleHeader:[\s\S]*?name:\s*(x-cosmosh-locale)/m, 'x-cosmosh-locale');
const authInvalidTokenCode = readFirstMatch(/enum:\s*\['(AUTH_INVALID_TOKEN)'\]/, 'AUTH_INVALID_TOKEN');
const testPingOkCode = readFirstMatch(/enum:\s*\['(TEST_PING_OK)'\]/, 'TEST_PING_OK');
const capabilities = extractCapabilities();

const formatPathEntries = pathEntries.map(([operationId, routePath]) => `  ${operationId}: '${routePath}',`).join('\n');
const formatCapabilities = capabilities.map((item) => `'${item}'`).join(', ');

const output = `/**\n * This file is auto-generated from openapi/cosmosh.openapi.yaml.\n * Do not edit this file directly. Run "pnpm --filter @cosmosh/api-contract generate" instead.\n */\n\nexport const API_PATHS = {\n${formatPathEntries}\n} as const;\n\nexport const API_HEADERS = {\n  internalToken: '${internalTokenHeaderName}',\n  locale: '${localeHeaderName}',\n} as const;\n\nexport const API_CODES = {\n  authInvalidToken: '${authInvalidTokenCode}',\n  testPingOk: '${testPingOkCode}',\n} as const;\n\nexport const API_CAPABILITIES = [${formatCapabilities}] as const;\n`;

fs.writeFileSync(protocolOutputPath, output, 'utf8');
console.log(`Generated protocol constants from OpenAPI -> ${path.relative(cwd, protocolOutputPath)}`);
