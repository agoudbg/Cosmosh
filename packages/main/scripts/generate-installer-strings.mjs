#!/usr/bin/env node
/**
 * generate-installer-strings.mjs
 *
 * Reads installer-specific strings from the i18n locale JSON files
 * (packages/i18n/locales/<lang>/installer.json) and generates a NSIS !define
 * include file (resources/installer-strings.nsh) so that installer.nsh can
 * reference translated strings without hard-coding them.
 *
 * Key design decisions:
 * - Uses NSIS !define (compile-time substitution) rather than LangString to
 *   avoid dependency on which MUI_LANGUAGE files electron-builder includes.
 * - The installer.nsh runtime code uses `$LANGUAGE == 2052` to pick the right
 *   define at runtime.
 * - {productName} placeholders are kept as-is; installer.nsh substitutes
 *   ${PRODUCT_NAME} (the electron-builder-defined NSIS variable) instead.
 *
 * Supported locales and their NSIS $LANGUAGE values:
 *   en       - 1033 (English, default / fallback)
 *   zh-CN    - 2052 (Simplified Chinese)
 */

import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));

// Paths relative to packages/main/
const LOCALES_DIR = join(__dirname, "..", "..", "..", "packages", "i18n", "locales");
const OUTPUT_FILE = join(__dirname, "..", "resources", "installer-strings.nsh");
const RESOURCES_DIR = join(__dirname, "..", "resources");

/** Locale config: locale folder name → NSIS suffix used in !define names */
const LOCALES = [
  { folder: "en", suffix: "EN" },
  { folder: "zh-CN", suffix: "ZH" },
];

/**
 * The installer strings live under the "installer" key of main.json, which is
 * co-located with other main-process i18n strings. Using a dedicated section
 * keeps the locale files consolidated while still being clearly scoped.
 */
const LOCALE_FILE = "main.json";
const INSTALLER_KEY = "installer";

/**
 * Flatten a nested JSON object into dot-notation keys.
 * e.g. { shortcutPage: { label: "foo" } } → { "shortcutPage.label": "foo" }
 * @param {Record<string, unknown>} obj
 * @param {string} prefix
 * @returns {Record<string, string>}
 */
function flattenObject(obj, prefix = "") {
  return Object.entries(obj).reduce((acc, [key, value]) => {
    const fullKey = prefix ? `${prefix}.${key}` : key;
    if (typeof value === "object" && value !== null) {
      Object.assign(acc, flattenObject(value, fullKey));
    } else {
      acc[fullKey] = String(value);
    }
    return acc;
  }, {});
}

/**
 * Convert a dot-notation i18n key to a NSIS !define name.
 * e.g. "shortcutPage.label" + "EN" → "COSMOSH_INST_SHORTCUT_PAGE_LABEL_EN"
 * @param {string} key
 * @param {string} localeSuffix
 * @returns {string}
 */
function toDefName(key, localeSuffix) {
  const snakeKey = key
    .replace(/\./g, "_")
    // Insert underscore before uppercase letters (camelCase → SNAKE_CASE)
    .replace(/([a-z])([A-Z])/g, "$1_$2")
    .toUpperCase();
  return `COSMOSH_INST_${snakeKey}_${localeSuffix}`;
}

/**
 * Escape a string for use as a NSIS !define value.
 * - Replace \n with $\r$\n (NSIS line-break in string arguments)
 * - Replace {productName} with ${PRODUCT_NAME} (NSIS compile-time variable)
 * @param {string} str
 * @returns {string}
 */
function escapeForNsis(str) {
  return str
    .replace(/\n/g, "$\\r$\\n")
    .replace(/\{productName\}/g, "${PRODUCT_NAME}");
}

// ── Main ──────────────────────────────────────────────────────────────────────

const allDefines = [];

for (const { folder, suffix } of LOCALES) {
  const filePath = join(LOCALES_DIR, folder, LOCALE_FILE);

  let raw;
  try {
    raw = readFileSync(filePath, "utf8");
  } catch {
    console.error(`[generate-installer-strings] Missing locale file: ${filePath}`);
    process.exit(1);
  }

  const data = JSON.parse(raw);
  const installerSection = data[INSTALLER_KEY];

  if (!installerSection) {
    console.error(
      `[generate-installer-strings] Missing "${INSTALLER_KEY}" key in ${filePath}`
    );
    process.exit(1);
  }

  const flat = flattenObject(installerSection);

  for (const [key, value] of Object.entries(flat)) {
    allDefines.push({
      name: toDefName(key, suffix),
      value: escapeForNsis(value),
    });
  }
}

// Build output content
const lines = [
  `; installer-strings.nsh`,
  `; AUTO-GENERATED by scripts/generate-installer-strings.mjs`,
  `; Do NOT edit manually – update packages/i18n/locales/<lang>/main.json (installer key) instead.`,
  ``,
  ...allDefines.map(({ name, value }) => `!define ${name} "${value}"`),
  ``,
];

// Ensure the resources directory exists (it should, but just in case)
mkdirSync(RESOURCES_DIR, { recursive: true });

writeFileSync(OUTPUT_FILE, lines.join("\n"), "utf8");
console.log(`[generate-installer-strings] Written ${allDefines.length} defines → ${OUTPUT_FILE}`);
